<h2 class="wp-block-heading has-base-color has-text-color has-link-color has-roboto-condensed-font-family" style="margin-top:0;font-size:1.6rem;font-style:normal;font-weight:700;line-height:1;text-transform:uppercase">Historia</h2>
<!-- /wp:heading -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":1.4}}} -->
<p style="line-height:1.4">El Museo Nacional de Bellas Artes fue inaugurado en diciembre de 1896 en el edificio de las tiendas Bon Marché de la calle Florida, hoy Galerías Pacífico. Desde su origen, se planteó como un espacio destinado a albergar arte internacional de todos los períodos históricos, y a promover y consolidar un arte argentino por entonces incipiente.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":1.4}}} -->
<p style="line-height:1.4">Hacia 1910, en épocas del Centenario de la Revolución de Mayo, el Museo ya contaba en su colección con piezas de los maestros Francisco de Goya, Joaquín Sorolla y Bastida, Edgar Degas y Pierre-Auguste Renoir.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":1.4}}} -->
<p style="line-height:1.4">En 1911, se inauguró la segunda sede del Bellas Artes: el Pabellón Argentino, una estructura monumental que el país había utilizado en la Exposición Universal de París de 1889 y que fue instalada en la Plaza San Martín. Allí, se exhibieron nuevas adquisiciones que ampliaron la colección, como La ninfa sorprendida, de Édouard Manet, y Orillas del Sena, de Claude Monet.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":1.4}}} -->
<p style="line-height:1.4">La institución fue trasladada en 1933 a su sede actual: la antigua Casa de Bombas de Recoleta, remodelada por el arquitecto Alejandro Bustillo. Durante esos años, se incorporaron destacadas piezas, entre ellas, Mujer del mar, de Paul Gauguin, Le Moulin de la Galette, de Vincent van Gogh, y Jesús en el huerto de los Olivos, de El Greco.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":1.4}}} -->
<p style="line-height:1.4">Las últimas décadas del siglo XX acogieron a grandes referentes del arte moderno internacional. Así, se sumaron obras de Pablo Picasso, Amedeo Modigliani, Marc Chagall, Vassily Kandinsky, Paul Klee, Lucio Fontana, Jackson Pollock, Mark Rothko y Henry Moore.</p>
