<h2 class="wp-block-heading">Comentario sobre Le bain de Vénus</h2>
<!-- /wp:heading -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":1.4}}} -->
<p style="line-height:1.4">Arte Siglo XIX (1800-1910)</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":1.4}}} -->
<p style="line-height:1.4">El título original de esta pintura puede haber sido puesto por Redon, ya que al parecer la obra fue comprada al artista sin intermediarios. Los toques de azul y verde en la zona inferior derecha de la tela pueden considerarse una representación del agua, y el tocado de la figura puede evocar un personaje mitológico.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":1.4}}} -->
<p style="line-height:1.4">Estos aspectos, sin embargo, son los únicos que justificarían el título de El baño de Venus. La figura no está desnuda, aparece (apenas) vestida y carece de otro atributo inherente a la diosa del amor. Su movimiento no corresponde a una escena de baño y el personaje demuestra una vacilación indigna de alguien nacido del mar. Con los dos brazos extendidos hacia delante y la cabeza hacia atrás, parece moverse de derecha a izquierda como si estuviese huyendo de algo –en una posición relativamente similar a la del dibujo Female Nude (Bridgestone Museum, Tokio).</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":1.4}}} -->
<p style="line-height:1.4">El origen de su miedo o resistencia parece ser la forma circular sobre la derecha (de la mitad del tamaño de la figura) que semeja más a un insecto o a uno de los monstruos submarinos pintados por Redon que a las flores y las hojas reconocibles sobre la izquierda. Esta interpretación se sustenta en el uso del negro para delinear y rellenar parte de esta forma, color asociado por Redon al mundo fantástico de sus primeras carbonillas y litografías.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"style":{"typography":{"lineHeight":1.4}}} -->
<p style="line-height:1.4">En lugar de Venus esta figura parece ser una de aquellas “mujeres angustiadas” señaladas por Gloria Groom en la obra tardía de Redon, como Andrómeda y Angélica, salvadas ambas de un dragón por Perseo y Rogelio respectivamente. El fondo indeterminado de esta pintura, con su nube de blanco, gris y azul en el área superior, se corresponde perfectamente con esas composiciones, por ejemplo con Roger and Angelica, ca. 1910 (The Museum of Modern Art, Nueva York) expuesta en el Armory Show de 1913.</p>
